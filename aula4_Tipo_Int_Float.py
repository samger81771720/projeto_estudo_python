# Tipo int(Números negativos, zero e os números positivos)
# O número está sendo escrito de forma literal abaixo
print(-11,0,11, sep = '      ')

# O tipo float representa qualquer número positivo ou negativo com ponto flutuante.
print(1.1, 0.0, -1.1)

''' type() --> Mesmo sendo iniciada com letra minúscula, "type" é uma classe que, 
dentre outras utilidades, retorna o nome do tipo da variável que o interpretador 
deduziu. '''

print(type(1))
print(type('André'))
print(type(1.1),type(2),type('André'))

