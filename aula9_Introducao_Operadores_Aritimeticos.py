adicao = 10.3 + 10  # O Python consegue efetuar operações aritiméticas entre "float" e "int"
print('Resultado da adição:', adicao)

subtracao = 9 - 3.2
print('Resultado da subtração:', subtracao)

multiplicacao = 6 * 9
print('Resultado da multiplicação:', multiplicacao)

divisao_comum = 9 / 3  # Sempre retornará um número de ponto flutuante - "float"
print('Resultado da divisão comum:', divisao_comum)

divisao_inteira = 8 // 4.2
print('Resultado da divisão inteira:', divisao_inteira)

exponenciacao = 2 ** 3  # "Dois elevado a três"
print('Resultado da exponenciação:', exponenciacao) 

modulo = 25 % 2  # Resto da divisão
print('Resultado do resto da divisão entre 25 e 2:', modulo)
print('Possui resto da divisão?', modulo > 0)







