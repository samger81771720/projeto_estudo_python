# se / se não se / se não

entrada = input('\nVocê quer "entrar" ou "sair"? ')

if entrada == 'entrar':
    print('\nVocê entrou no sistema.\n')
elif entrada == 'sair':
    print('\nVocê saiu do sistema.\n')
else:
    print('\nVocê não digitou nem "entrar" e nem "sair"!\n')
